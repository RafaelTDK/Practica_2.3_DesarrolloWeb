import mongoose from "mongoose";

const ClienteSchema = new mongoose.Schema({
    _id: {
        type: String,
        required: true,
    },
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    address: {
        type: String,
        required: true,
    },
    city: {
        type: String,
        required: true,
    },
    country: {
        type: String,
        required: true,
    }
});

const ClienteModel = mongoose.model("Cliente", ClienteSchema);
export default ClienteModel;
